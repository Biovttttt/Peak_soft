package Home_work_13;

public class Class {
    private int number;
    private String word;
    private int massiv [];

    Class(int number, String word, int[] massiv) {
        this.number = number;
        this.word = word;
        this.massiv = massiv;
    }

    public int getNumber() {
        return number;
    }

    public String getWord() {
        return word;
    }

    public int[] getMassiv() {
        return massiv;
    }
}
